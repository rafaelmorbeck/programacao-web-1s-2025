let x = 5
let y = 2
let resultado;

//soma
resultado = x + y;
console.log(`Resultado da soma entre ${x} e ${y} é ${resultado}`);

//subtracao
resultado = x - y;
console.log(`Resultado da subtracao entre ${x} e ${y} é ${resultado}`)

//multiplicacao
resultado = x * y;
console.log(`Resultado da multiplicacao entre ${x} e ${y} é ${resultado}`)

//divisao
resultado = x/y;
console.log(`Resultado da divisao entre ${x} e ${y} é ${resultado}`)